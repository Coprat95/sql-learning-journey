-- FIRST_VALUE Y LAST_VALUE ( PRIMER VALOR Y ÃšLTIMO)
SELECT 
  titulo,
  FIRST_VALUE(titulo) OVER (ORDER BY popularidad DESC ) AS MasPopular,
  
  LAST_VALUE(titulo) OVER (ORDER BY popularidad DESC ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING) AS MenosPopular
FROM canciones;

